package uz.perfectalgorithm.projects.tezkor.utils.log_messages

import android.util.Log

fun myLogD(message: String, tag: String = "TAG19") {
    Log.d(tag, message)
}


fun myLogE(message: String, tag: String = "TAG19") {
    Log.e(tag, message)
}


fun myLogI(message: String, tag: String = "TAG19") {
    Log.i(tag, message)
}